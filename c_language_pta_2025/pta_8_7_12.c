
// 7-12 加密数

// 题意就是：把每个输入视为一个32位有符号整数，但“解密”时按它的无符号二进制表示分成4个字节（从高到低每8位），各字节按无符号取值相加并输出。

// 关键点：

// 32位整数有4个字节，每字节范围0–255。解密值最大为 255*4=1020。
// 负数用补码存储，例如 -1 是 0xFF FF FF FF，4个字节各为255，总和1020。
// 要用无符号位运算提取字节，避免负数右移的符号扩展影响：先转为 unsigned，再用 >> 和 & 0xFF。
// 示例解释：

// 65920 = 0x00 01 01 80 → 0 + 1 + 1 + 128 = 130
// -1 = 0xFF FF FF FF → 255 + 255 + 255 + 255 = 1020

#include <stdio.h>

int main(void)
{
    int x; // 题目给定：32位 int
    while (scanf("%d", &x) == 1)
    {
        unsigned int u = (unsigned int)x; // 视为32位无符号以避免算术右移影响
        int sum = 0;
        sum += (u & 0xFFu);
        sum += ((u >> 8) & 0xFFu);
        sum += ((u >> 16) & 0xFFu);
        sum += ((u >> 24) & 0xFFu);
        printf("%d\n", sum);
    }
    return 0;
}